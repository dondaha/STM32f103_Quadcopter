void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart) {
    /* Prevent unused argument(s) compilation warning */
    /* NOTE: This function Should not be modified, when the callback is needed,
             the HAL_UART_TxCpltCallback could be implemented in the user file
     */
    if (huart == &huart2) {
        // 解析数据包，一共8个字节，第一个字节为'C'，第二个字节解析为uint8_t的数字，代表油门值。第八位'0x0D为截止符号。中间不解析
        if (aRxBuffer == 0xD0||Rx_Cnt>=RXBUFFERSIZE) {
            Rx_Cnt = 0;
            RxBuffer[Rx_Cnt++] = aRxBuffer;
        } else if (aRxBuffer == 0x0D) {
            RxBuffer[Rx_Cnt++] = aRxBuffer;
            // 解析数据

            parse_command(RxBuffer);
            // 重置接收缓冲
            Rx_Cnt = 0;
            // 发送数据
//            printf("Throttle: %d\r\n", throttle);
        } else {
            RxBuffer[Rx_Cnt++] = aRxBuffer;
        }
    }
    HAL_UART_Receive_IT(&huart2, (uint8_t *) &aRxBuffer, 1);   //因为接收中断使用了一次即关闭，所以在最后加入这行代码即可实现无限使用
}
